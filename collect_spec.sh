#!/bin/bash
date
hostname
echo "[cpu]============================================================================================================"
lscpu
echo "[mem]============================================================================================================"
free -m
cat /proc/meminfo | grep -e "MemTotal" -e "MemFree" -e "Buffers" -e "Cached" -e "SwapTotal" -e "SwapFree" -e "Slab"
echo "[partition]======================================================================================================"
fdisk -l
echo "[filesystem]====================================================================================================="
df -Th
echo "[OS-kernel]======================================================================================================"
uname -a
echo "[OS-release]====================================================================================================="
cat /etc/system-release
echo "[locale]========================================================================================================="
cat /etc/locale.conf
echo "[auth(su)]======================================================================================================="
cat /etc/pam.d/su
echo "[auth(sudo)]====================================================================================================="
cat /etc/sudoers
cat /etc/sudoers.d/*
echo "[groups]========================================================================================================="
cat /etc/group
echo "[users]=========================================================================================================="
cat /etc/passwd
echo "[selinux]========================================================================================================"
getenforce
echo "[network(ip-interface)]=========================================================================================="
ip a
echo "[network(routing-table)]========================================================================================="
netstat -rn
echo "[resolver(priority)]============================================================================================="
cat /etc/nsswitch.conf | grep "hosts: "
echo "[resolver(hosts)]================================================================================================"
cat /etc/hosts
echo "[resolver(dns resolver)]========================================================================================="
cat /etc/resolv.conf
echo "[yum-repository]================================================================================================="
ls -la /etc/yum.repos.d
echo "[packages(yum)]=================================================================================================="
yum list installed
echo "[packages(rpm)]=================================================================================================="
rpm -qa
echo "[services]======================================================================================================="
systemctl list-unit-files -t service
echo "[sshd]==========================================================================================================="
cat /etc/ssh/sshd_config
echo "[iptables]======================================================================================================="
iptables -L
echo "[cron]==========================================================================================================="
cat /var/spool/cron/*
echo "[logrotate]======================================================================================================"
cat /etc/logrotate.conf
echo "[ntp]============================================================================================================"
ntpq -p
echo "[kernel-params]=================================================================================================="
sysctl -a
