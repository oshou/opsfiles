#!/bin/bash


echo "[sw-clock]======================================================================================================="
date
echo "[hw-clock]======================================================================================================="
hwclock
echo "[id]============================================================================================================="
id
echo "[cpu]============================================================================================================"
lscpu | grep -e "アーキテクチャ" -e "Byte Order" -e "CPU(s):" -e "スレッド数" -e "コア数" -e "MHz" -e "Model name" -e " キャッシュ"
echo "[mem]============================================================================================================"
free -m
cat /proc/meminfo | grep -e "MemTotal" -e "MemFree" -e "Buffers" -e "Cached" -e "SwapTotal" -e "SwapFree" -e "Slab"
echo "[partition]======================================================================================================"
fdisk -l
echo "[filesystem]====================================================================================================="
df -Th
echo "[OS-kernel]======================================================================================================"
uname -a
echo "[OS-distribution]================================================================================================"
cat /etc/system-release
echo "[hostname]======================================================================================================="
hostname
echo "[locale]========================================================================================================="
cat /etc/locale.conf
echo "[selinux]========================================================================================================"
getenforce
echo "[resolve-priority]==============================================================================================="
cat /etc/nsswitch.conf | grep "hosts: "
echo "[resolver-localhosts]============================================================================================"
cat /etc/hosts
echo "[resolver-resolver]=============================================================================================="
cat /etc/resolv.conf
echo "[yum-repo]======================================================================================================="
ls -la /etc/yum.repos.d
echo "[yum-packages]==================================================================================================="
yum list installed
echo "[rpm-packages]==================================================================================================="
rpm -qa
echo "[groups]========================================================================================================="
cat /etc/group
echo "[users]=========================================================================================================="
cat /etc/passwd
echo "[service(enabled)]==============================================================================================="
systemctl list-unit-files -t service | grep enabled
echo "[sshd]==========================================================================================================="
cat /etc/ssh/sshd_config | egrep '^PasswordAuthentication|^PubkeyAuthentication|^PermitRootLogin'
echo "[cron]==========================================================================================================="
cat /var/spool/cron/*
echo "[ntp]============================================================================================================"
ntpq -p
